print("python punya 3 tipe numerik: int, float, and complex")

#tipe data integerer 
myValue = 23
print(myValue)
print(type(myValue))
print(str(myValue) + " adalah umur saya ditahun 2025")

#tipe data float
dataFloat = 3.14
print(dataFloat)
print(type(dataFloat))
print(str(dataFloat) + " adalah pi sebuah lingkaran")

#tipe data complex
dataComplex = 5j
